package eu.europa.ec.digit.leos.pilot.export.config;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.boot.web.client.RestTemplateBuilder;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.client.RestTemplate;
import org.springframework.web.servlet.view.freemarker.FreeMarkerConfigurer;

import java.nio.charset.StandardCharsets;

@Configuration
public class BeansConfig {
    private final static Logger LOG = LoggerFactory.getLogger(BeansConfig.class);

    @Bean
    public FreeMarkerConfigurer freeMarkerConfigurer() {
        LOG.debug("FreeMarkerConfigurer bean");
        FreeMarkerConfigurer freeMarkerConfigurer = new FreeMarkerConfigurer();
        freeMarkerConfigurer.setTemplateLoaderPath("classpath:/templates");
        freeMarkerConfigurer.setDefaultEncoding(StandardCharsets.UTF_8.name());
        return freeMarkerConfigurer;
    }

    @Bean
    public RestTemplate restTemplate(RestTemplateBuilder builder) {
       return builder.build();
    }
}
